//Program to print size of an int, a float, a char and a double type variable

#include<stdio.h>
int main()
{
    int i;
    float f;
    char c;
    double d;
    printf("Size of i is %d",sizeof(i));
    printf("\nSize of f is %d",sizeof(f));
    printf("\nSize of c is %d",sizeof(c));
    printf("\nSize of d is %d",sizeof(d));
}